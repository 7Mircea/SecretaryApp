package com.example.bazadedateexplo;

public class ClubClass {
    private int idClub;
    private String nume;
    private int idConferinta;
    private int idConducator;

    public int getIdClub() {
        return idClub;
    }

    public void setIdClub(int idClub) {
        this.idClub = idClub;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public int getIdConferinta() {
        return idConferinta;
    }

    public void setIdConferinta(int idConferinta) {
        this.idConferinta = idConferinta;
    }

    public int getIdConducator() {
        return idConducator;
    }

    public void setIdConducator(int idConducator) {
        this.idConducator = idConducator;
    }
}
