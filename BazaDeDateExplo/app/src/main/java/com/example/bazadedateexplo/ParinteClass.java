package com.example.bazadedateexplo;

public class ParinteClass {
    int idParinte;
    String nume;
    String prenume;
    String gen;
    String nrDeTelefon;

    public int getIdParinte() {
        return idParinte;
    }

    public void setIdParinte(int idParinte) {
        this.idParinte = idParinte;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getPrenume() {
        return prenume;
    }

    public void setPrenume(String prenume) {
        this.prenume = prenume;
    }

    public String getGen() {
        return gen;
    }

    public void setGen(String gen) {
        this.gen = gen;
    }

    public String getNrDeTelefon() {
        return nrDeTelefon;
    }

    public void setNrDeTelefon(String nrDeTelefon) {
        this.nrDeTelefon = nrDeTelefon;
    }
}
